"""
app/services/severity_prediction_service.py
Business logic for ML severity prediction.

Converts the incoming Pydantic request to a feature dict, calls the model,
applies safety rules, and returns a versioned response.

Logging:
  - Safe metadata only (emergency_type, age_group, booleans).
  - Description text is NEVER logged.
  - User ID is logged only at DEBUG level as a safe truncated prefix.
"""

from __future__ import annotations

import logging
from uuid import UUID

from app.schemas.severity_prediction import SeverityPredictionRequest, SeverityPredictionResponse

logger = logging.getLogger("medicare.services.severity_prediction")

_DISCLAIMER = (
    "This severity suggestion is generated by a prototype model trained on synthetic data. "
    "It carries no clinical validity. The user must confirm the severity before submitting. "
    "Always contact emergency services for life-threatening situations."
)


def _extract_important_factors(
    features: dict,
    safety_override: bool,
    override_reason: str | None,
) -> list[str]:
    """Return a short list of human-readable contributing factors (UI display only)."""
    factors: list[str] = []

    if safety_override and override_reason:
        factors.append(f"Safety rule: {override_reason}")

    bool_flags = [
        ("severe_breathing_difficulty", "Severe breathing difficulty"),
        ("breathing_difficulty", "Breathing difficulty"),
        ("chest_pain", "Chest pain"),
        ("seizure", "Seizure"),
        ("stroke_signs", "Stroke signs"),
        ("allergic_reaction", "Allergic reaction"),
        ("pregnancy_emergency", "Pregnancy emergency"),
        ("major_accident", "Major accident"),
        ("violence_risk", "Violence risk"),
    ]
    for flag, label in bool_flags:
        if features.get(flag):
            factors.append(label)

    bleeding = features.get("bleeding_level", "none")
    if bleeding not in (None, "none", 0, ""):
        factors.append(f"Bleeding: {bleeding}")

    burn = features.get("burn_level", "none")
    if burn not in (None, "none", 0, ""):
        factors.append(f"Burns: {burn}")

    if features.get("conscious") == 0:
        factors.append("Unconscious")

    factors.append("Description text analysis")
    return factors[:5]


class SeverityPredictionService:
    """Orchestrates feature extraction → model inference → safety rules → response."""

    def predict(
        self,
        request: SeverityPredictionRequest,
        user_id: UUID | str,
        confidence_threshold: float = 0.65,
    ) -> SeverityPredictionResponse:
        from ml.severity.src.model_registry import ModelRegistry, ModelUnavailableError
        from ml.severity.src.preprocessing import (
            VALID_AGE_GROUPS,
            VALID_BLEED_LEVELS,
            VALID_BURN_LEVELS,
            VALID_TYPES,
            normalize_boolean,
            normalize_category,
        )
        from ml.severity.src.safety_rules import apply_safety_rules
        from app.schemas.database.emergency_request import EmergencySeverity

        # ── Load registry ─────────────────────────────────────────────────
        registry = ModelRegistry.get()  # raises ModelUnavailableError if missing

        # ── Build feature dict ────────────────────────────────────────────
        bleeding = request.bleeding_level.value if request.bleeding_level else "none"
        burn = request.burn_level.value if request.burn_level else "none"
        age = request.age_group.value if request.age_group else "unknown"
        etype = request.emergency_type.value

        # conscious defaults to True (1) when not provided
        conscious_val = normalize_boolean(
            request.conscious if request.conscious is not None else True
        )

        features: dict = {
            "description": request.description.strip().lower(),
            "emergency_type": normalize_category(etype, VALID_TYPES, "other"),
            "age_group": normalize_category(age, VALID_AGE_GROUPS, "unknown"),
            "bleeding_level": normalize_category(bleeding, VALID_BLEED_LEVELS, "none"),
            "burn_level": normalize_category(burn, VALID_BURN_LEVELS, "none"),
            "conscious": conscious_val,
            "breathing_difficulty": normalize_boolean(request.breathing_difficulty),
            "severe_breathing_difficulty": normalize_boolean(request.severe_breathing_difficulty),
            "chest_pain": normalize_boolean(request.chest_pain),
            "seizure": normalize_boolean(request.seizure),
            "stroke_signs": normalize_boolean(request.stroke_signs),
            "allergic_reaction": normalize_boolean(request.allergic_reaction),
            "pregnancy_emergency": normalize_boolean(request.pregnancy_emergency),
            "major_accident": normalize_boolean(request.major_accident),
            "violence_risk": normalize_boolean(request.violence_risk),
        }

        logger.debug(
            "Severity prediction — user=%s type=%s age=%s",
            str(user_id)[:8],
            etype,
            age,
        )

        # ── Model inference ───────────────────────────────────────────────
        raw_severity, confidence = registry.model.predict(features)

        # ── Safety rule indicators (conscious=True means OK; False = risk) ─
        indicator_dict = {
            "severe_breathing_difficulty": bool(features["severe_breathing_difficulty"]),
            "breathing_difficulty": bool(features["breathing_difficulty"]),
            "conscious": features["conscious"] == 0,  # invert: True here means unconscious
            "bleeding_level": bleeding,
            "burn_level": burn,
            "chest_pain": bool(features["chest_pain"]),
            "seizure": bool(features["seizure"]),
            "stroke_signs": bool(features["stroke_signs"]),
            "allergic_reaction": bool(features["allergic_reaction"]),
            "pregnancy_emergency": bool(features["pregnancy_emergency"]),
            "major_accident": bool(features["major_accident"]),
            "violence_risk": bool(features["violence_risk"]),
        }

        safety_result = apply_safety_rules(raw_severity, indicator_dict)
        final_severity = safety_result.final_severity

        # ── Assemble response ─────────────────────────────────────────────
        low_confidence = confidence is not None and confidence < confidence_threshold
        factors = _extract_important_factors(
            features, safety_result.override_applied, safety_result.override_reason
        )

        return SeverityPredictionResponse(
            predicted_severity=EmergencySeverity(final_severity),
            raw_model_severity=EmergencySeverity(raw_severity),
            confidence=round(confidence, 3) if confidence is not None else None,
            model_version=registry.version,
            important_factors=factors,
            safety_override_applied=safety_result.override_applied,
            safety_override_reason=safety_result.override_reason,
            low_confidence=low_confidence,
            disclaimer=_DISCLAIMER,
        )
